    //MÉTODO PARA VALIDAR OS CAMPOS.
    public boolean ValidaCampo(){
        boolean valida = true;
        String senha = String.valueOf(tx_senha.getPassword());
        if(tx_login.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campo Login Vazio","Alerta",JOptionPane.WARNING_MESSAGE);
            tx_login.requestFocus();
            return false;
        }else if(senha.equals("")){
            JOptionPane.showMessageDialog(null, "Campo Senha Vazio","Alerta",JOptionPane.WARNING_MESSAGE);
            tx_senha.requestFocus();
            return false;
        }
        return valida;
    }

